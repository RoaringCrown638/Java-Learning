public class Print {
    static void print(String text) {
        System.out.println(text);
    }

    static void print(int text) {
        System.out.println(text);
    }

    static void print(float text) {
        System.out.println(text);
    }

    static void print(double text) {
        System.out.println(text);
    }
}
